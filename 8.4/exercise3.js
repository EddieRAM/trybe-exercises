function averageAge() {
  // escreva seu código aqui
  // 1. Retornar a idade que os autores tinham quando os livros foram lançados em um array através do map();
  const authorAgeArr = books.map((book) => book.releaseYear - book.author.birthYear);
  // 2. Retornar o acumulador a ser utilizado como parâmetro no reduce, que pode ser o somatório de todas as idades;
  const ageSum = authorAgeArr.reduce((age, currentValue) => age + currentValue);
  // 3. Agora é só dividir pelo número de ítens do array.
  const averageAge = ageSum / authorAgeArr.length;
  return averageAge;
}

